napi_value initModulesBinding(napi_env env, napi_value exports) {
  // compileCacheStatus: array of status name strings.
  // JS side (helpers.js) iterates this to build a name->index mapping.
  {
    const char *statusNames[] = {
        "FAILED", "ENABLED", "ALREADY_ENABLED", "DISABLED"};
    constexpr size_t count = sizeof(statusNames) / sizeof(statusNames[0]);
    napi_value arr;
    NAPI_CALL(napi_create_array_with_length(env, count, &arr));
    for (size_t i = 0; i < count; ++i) {
      napi_value str;
      NAPI_CALL(
          napi_create_string_utf8(env, statusNames[i], NAPI_AUTO_LENGTH, &str));
      NAPI_CALL(napi_set_element(env, arr, static_cast<uint32_t>(i), str));
    }
    NAPI_CALL(
        napi_set_named_property(env, exports, "compileCacheStatus", arr));
  }

  // Register stub functions.
  struct {
    const char *name;
    napi_callback cb;
  } funcs[] = {
      {"enableCompileCache", enableCompileCacheCb},
      {"getCompileCacheDir", getCompileCacheDirCb},
      {"flushCompileCache", flushCompileCacheCb},
      {"readPackageJSON", readPackageJSONCb},
      {"getPackageScopeConfig", getPackageScopeConfigCb},
      {"getPackageType", getPackageTypeCb},
      {"getNearestParentPackageJSONType", getNearestParentPackageJSONTypeCb},
      {"setLazyPathHelpers", setLazyPathHelpersCb},
  };

  for (const auto &f : funcs) {
    napi_value fn;
    NAPI_CALL(napi_create_function(
        env, f.name, NAPI_AUTO_LENGTH, f.cb, nullptr, &fn));
    NAPI_CALL(napi_set_named_property(env, exports, f.name, fn));
  }

  return exports;
}

