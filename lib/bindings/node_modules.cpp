#define NAPI_CALL(call)                                             \
  do {                                                              \
    napi_status status_ = (call);                                   \
    if (status_ != napi_ok) {                                       \
      napi_throw_error(env, nullptr, "NAPI call failed in " #call); \
      return nullptr;                                               \
    }                                                               \
  } while (0)

/// enableCompileCache(cacheDir) -> {status, directory, message}
/// Stub: always returns status=0 (FAILED), empty directory.
static napi_value
enableCompileCacheCb(napi_env env, napi_callback_info /*info*/) {
  napi_value result;
  NAPI_CALL(napi_create_object(env, &result));

  napi_value statusVal;
  NAPI_CALL(napi_create_int32(env, 0, &statusVal));
  NAPI_CALL(napi_set_named_property(env, result, "status", statusVal));

  napi_value dirVal;
  NAPI_CALL(napi_create_string_utf8(env, "", 0, &dirVal));
  NAPI_CALL(napi_set_named_property(env, result, "directory", dirVal));

  return result;
}

