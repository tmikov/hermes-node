/// readPackageJSON(path, isESM, base, specifier) ->
///   [name, main, type, imports, exports, file_path] | undefined
///
/// Reads and parses a package.json file from disk. Returns a 6-element array
/// matching Node's native API contract (see package_json_reader.js), or
/// undefined if the file does not exist or cannot be read.
///
/// - name: string or undefined
/// - main: string or undefined
/// - type: "commonjs", "module", or "none"
/// - imports: raw JSON string (for objects/arrays) or string, or undefined
/// - exports: raw JSON string (for objects/arrays) or string, or undefined
/// - file_path: the input path echoed back
static napi_value readPackageJSONCb(napi_env env, napi_callback_info info) {
  size_t argc = 4;
  napi_value argv[4];
  NAPI_CALL(napi_get_cb_info(env, info, &argc, argv, nullptr, nullptr));

  napi_value undef;
  NAPI_CALL(napi_get_undefined(env, &undef));

  if (argc < 1)
    return undef;

  // arg[0] = path (string, required).
  napi_valuetype pathType;
  NAPI_CALL(napi_typeof(env, argv[0], &pathType));
  if (pathType != napi_string)
    return undef;

  std::string path = getString(env, argv[0]);
  if (path.empty())
    return undef;

  // Read file from disk.
  std::string content;
  if (!readFileSync(path, content))
    return undef;

  // Strip UTF-8 BOM if present.
  if (content.size() >= 3 && content[0] == '\xEF' && content[1] == '\xBB' &&
      content[2] == '\xBF') {
    content.erase(0, 3);
  }

  // Get JSON.parse and JSON.stringify from the global object.
  napi_value global, jsonObj, parseFn, stringifyFn;
  NAPI_CALL(napi_get_global(env, &global));
  NAPI_CALL(napi_get_named_property(env, global, "JSON", &jsonObj));
  NAPI_CALL(napi_get_named_property(env, jsonObj, "parse", &parseFn));
  NAPI_CALL(napi_get_named_property(env, jsonObj, "stringify", &stringifyFn));

  // Parse JSON.
  napi_value contentStr;
  NAPI_CALL(napi_create_string_utf8(
      env, content.data(), content.size(), &contentStr));

  napi_value parsed;
  if (napi_call_function(env, jsonObj, parseFn, 1, &contentStr, &parsed) !=
      napi_ok) {
    // JSON.parse threw a SyntaxError -- let the exception propagate.
    return nullptr;
  }

  // Ensure the parsed value is an object.
  napi_valuetype parsedType;
  NAPI_CALL(napi_typeof(env, parsed, &parsedType));
  if (parsedType != napi_object) {
    napi_throw_error(
        env,
        "ERR_INVALID_PACKAGE_CONFIG",
        "Invalid package config: must be a JSON object");
    return nullptr;
  }

  // --- Extract fields ---

  // name: string or undefined.
  napi_value nameVal;
  {
    napi_value val;
    NAPI_CALL(napi_get_named_property(env, parsed, "name", &val));
    napi_valuetype vt;
    NAPI_CALL(napi_typeof(env, val, &vt));
    nameVal = (vt == napi_string) ? val : undef;
  }

  // main: string or undefined.
  napi_value mainVal;
  {
    napi_value val;
    NAPI_CALL(napi_get_named_property(env, parsed, "main", &val));
    napi_valuetype vt;
    NAPI_CALL(napi_typeof(env, val, &vt));
    mainVal = (vt == napi_string) ? val : undef;
  }

  // type: "commonjs", "module", or "none".
  napi_value typeVal;
  {
    napi_value val;
    NAPI_CALL(napi_get_named_property(env, parsed, "type", &val));
    napi_valuetype vt;
    NAPI_CALL(napi_typeof(env, val, &vt));
    if (vt == napi_string) {
      std::string typeStr = getString(env, val);
      if (typeStr == "commonjs" || typeStr == "module") {
        typeVal = val;
      } else {
        NAPI_CALL(
            napi_create_string_utf8(env, "none", NAPI_AUTO_LENGTH, &typeVal));
      }
    } else {
      NAPI_CALL(
          napi_create_string_utf8(env, "none", NAPI_AUTO_LENGTH, &typeVal));
    }
  }

  // imports / exports: return as raw JSON string for objects/arrays,
  // as-is for strings, undefined otherwise.
  // This matches Node's behavior -- the JS side lazily parses these.
  auto extractJsonField = [&](const char *fieldName) -> napi_value {
    napi_value val;
    napi_get_named_property(env, parsed, fieldName, &val);
    napi_valuetype vt;
    napi_typeof(env, val, &vt);
    if (vt == napi_undefined || vt == napi_null)
      return undef;
    if (vt == napi_string)
      return val;
    if (vt == napi_object) {
      // Object or array -- stringify it so JS can lazily re-parse.
      napi_value stringified;
      if (napi_call_function(
              env, jsonObj, stringifyFn, 1, &val, &stringified) != napi_ok)
        return nullptr;
      return stringified;
    }
    // Other types (number, boolean) -- ignore.
    return undef;
  };

  napi_value importsVal = extractJsonField("imports");
  if (!importsVal)
    return nullptr;

  napi_value exportsVal = extractJsonField("exports");
  if (!exportsVal)
    return nullptr;

  // file_path: echo back the input path.
  napi_value filePath;
  NAPI_CALL(napi_create_string_utf8(env, path.data(), path.size(), &filePath));

  // Build 6-element result array: [name, main, type, imports, exports, path].
  napi_value result;
  NAPI_CALL(napi_create_array_with_length(env, 6, &result));
  NAPI_CALL(napi_set_element(env, result, 0, nameVal));
  NAPI_CALL(napi_set_element(env, result, 1, mainVal));
  NAPI_CALL(napi_set_element(env, result, 2, typeVal));
  NAPI_CALL(napi_set_element(env, result, 3, importsVal));
  NAPI_CALL(napi_set_element(env, result, 4, exportsVal));
  NAPI_CALL(napi_set_element(env, result, 5, filePath));

  return result;
}

