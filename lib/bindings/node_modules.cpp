#define NAPI_CALL(call)                                             \
  do {                                                              \
    napi_status status_ = (call);                                   \
    if (status_ != napi_ok) {                                       \
      napi_throw_error(env, nullptr, "NAPI call failed in " #call); \
      return nullptr;                                               \
    }                                                               \
  } while (0)

/// Get a UTF-8 string from a napi_value. Returns empty string if not a string.
static std::string getString(napi_env env, napi_value val) {
  napi_valuetype vtype;
  napi_typeof(env, val, &vtype);
  if (vtype != napi_string)
    return {};
  size_t len = 0;
  napi_get_value_string_utf8(env, val, nullptr, 0, &len);
  std::string result(len, '\0');
  napi_get_value_string_utf8(env, val, &result[0], len + 1, &len);
  return result;
}

/// Read a file synchronously into a string. Returns true on success.
static bool readFileSync(const std::string &path, std::string &content) {
  uv_fs_t req;

  // Open.
  int fd = uv_fs_open(nullptr, &req, path.c_str(), O_RDONLY, 0, nullptr);
  uv_fs_req_cleanup(&req);
  if (fd < 0)
    return false;

  // Stat for size.
  int64_t fileSize = 0;
  if (uv_fs_fstat(nullptr, &req, fd, nullptr) == 0)
    fileSize = req.statbuf.st_size;
  uv_fs_req_cleanup(&req);

  // Read.
  if (fileSize > 0) {
    content.resize(static_cast<size_t>(fileSize));
    uv_buf_t buf =
        uv_buf_init(&content[0], static_cast<unsigned int>(fileSize));
    int bytesRead = uv_fs_read(nullptr, &req, fd, &buf, 1, 0, nullptr);
    uv_fs_req_cleanup(&req);
    if (bytesRead < 0) {
      uv_fs_close(nullptr, &req, fd, nullptr);
      uv_fs_req_cleanup(&req);
      return false;
    }
    content.resize(static_cast<size_t>(bytesRead));
  } else {
    // Size unknown (e.g., /proc files). Read in chunks.
    char chunk[4096];
    for (;;) {
      uv_buf_t buf = uv_buf_init(chunk, sizeof(chunk));
      int bytesRead = uv_fs_read(nullptr, &req, fd, &buf, 1, -1, nullptr);
      uv_fs_req_cleanup(&req);
      if (bytesRead <= 0)
        break;
      content.append(chunk, static_cast<size_t>(bytesRead));
    }
  }

  // Close.
  uv_fs_close(nullptr, &req, fd, nullptr);
  uv_fs_req_cleanup(&req);
  return true;
}

/// enableCompileCache(cacheDir) -> {status, directory, message}
/// Stub: always returns status=0 (FAILED), empty directory.
static napi_value enableCompileCacheCb(
    napi_env env,
    napi_callback_info /*info*/) {
  napi_value result;
  NAPI_CALL(napi_create_object(env, &result));

  napi_value statusVal;
  NAPI_CALL(napi_create_int32(env, 0, &statusVal));
  NAPI_CALL(napi_set_named_property(env, result, "status", statusVal));

  napi_value dirVal;
  NAPI_CALL(napi_create_string_utf8(env, "", 0, &dirVal));
  NAPI_CALL(napi_set_named_property(env, result, "directory", dirVal));

  return result;
}

